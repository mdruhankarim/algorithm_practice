/*C. Айбар және сникерстер
time limit per test1 second
memory limit per test256 megabytes
Сникерстердің екі түрі бар: a
 теңгеге қосарланған, ал b
 (b<a)
 теңгеге жалғыз. Айбарда d
 теңге бар. Айбар n
 күн ішінде әр күн сайын сникерс жегісі келеді (маңызды емес, жалғыз немесе қос). Мұғалім Айбардан қос сникерстің ең көп дегенде қанша мөлшерін жеуге болатындығын білуді сұрады. Егер Айбар n
 күн ішінде әр күн сайын сникерс сатып ала алмаса, онда ол "Sorry" дейді. Айбар, әрине, тапсырманы еш қиындықсыз орындады. Сіз орындай аласыз ба?

Input
Бірінші жолда бір бүтін сан жазылады - q
 (1≤q≤104)
 — сұрақтар саны. Сұрақтың жалғыз жолында төрт бүтін сан бар – d
, n
, a
 және b
 (1≤d,n≤109,1≤b<a≤109)
.

Output
Әрбір сұрақ үшін, егер ол n
 күн ішінде сникерстерді сатып ала алмаса "Sorry"-ді немесе ол жеуге болатын қос сникерстердің максималды санын шығарыңыз.

Example
InputCopy
5
20 4 6 2
15 5 5 2
15 5 5 1
15 5 4 3
15 5 5 4
OutputCopy
3
1
2
0
Sorry
Note
Бірінші сұрақ мысалында Айбар 3 Қос сникерс пен 1 жалғыз сникерс сатып алады. Барлығы жұмсайды 3 * 6 + 1 * 2 = 20 теңге.

Екінші сұрақ мысалында Айбар 1 Қос сникерс пен 4 жалғыз сникерс сатып алады. Барлығы жұмсайды 1 * 5 + 4 * 2 = 13 теңге.


*/
#include<bits/stdc++.h>
using namespace std;
int main()
{
    int q;
    cin >> q;
    while (q--)
    {
        long long d, n, a, b;
        cin >> d >> n >> a >> b;
        if (n * b > d) {
            cout << "Sorry" << endl;
            continue;
        }
        long long max_double = min(n, d / a);
        long long remaining_money = d - max_double * a;
        long long single_needed = n - max_double;
        if (single_needed * b <= remaining_money) {
            cout << max_double << endl;
        } else {
            cout << "Sorry" << endl;
        }
    }
    return 0;
}